def example_grid_two_rooms() -> np.ndarray:
    """Two offset, interconnected rooms with repeated MNIST observations.

    The layout mirrors the two-room schematic: two 8x6 rooms are offset so the
    first room's lower-right 3x3 patch is the same physical/observational
    pattern as the second room's upper-left 3x3 patch. Cells outside the rooms
    are walls. Repeated digits create aliased observations across and within
    rooms, while the shared patch is a true connection between them.
    """
    grid = -np.ones((13, 9), dtype=np.int64)

    first_room = np.array(
        [
            [8, 4, 0, 1, 2, 3],
            [9, 4, 5, 6, 0, 7],
            [9, 2, 8, 6, 7, 4],
            [4, 0, 8, 9, 3, 7],
            [4, 6, 9, 1, 2, 0],
            [9, 8, 7, 1, 5, 6],
            [0, 0, 9, 3, 5, 6],
            [4, 7, 3, 0, 8, 7],
        ],
        dtype=np.int64,
    )
    second_room = np.array(
        [
            [1, 5, 6, 4, 6, 0],
            [3, 5, 6, 7, 9, 2],
            [0, 8, 7, 4, 3, 1],
            [9, 2, 4, 7, 6, 8],
            [1, 0, 5, 3, 9, 8],
            [8, 7, 3, 6, 4, 2],
            [5, 9, 1, 0, 8, 4],
            [6, 2, 7, 5, 3, 0],
        ],
        dtype=np.int64,
    )

    # First room: rows 0..7, cols 0..5.
    # Second room: rows 5..12, cols 3..8.
    # The 3x3 overlap at first[5:8, 3:6] == second[0:3, 0:3].
    grid[0:8, 0:6] = first_room
    grid[5:13, 3:9] = second_room
    return grid
